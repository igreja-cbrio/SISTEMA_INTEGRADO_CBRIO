  service_type_id, service_type_name, data_culto, recurrence_time,
  presencial_adulto, presencial_kids,
  decisoes_presenciais, decisoes_online,
  online_pico, online_ds, online_ddus, voluntarios
FROM slots
ON CONFLICT (service_type_id, data) DO UPDATE SET
  presencial_adulto    = EXCLUDED.presencial_adulto,
