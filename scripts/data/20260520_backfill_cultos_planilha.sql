-- Resolve service_type_id por padrão (recurrence_day + recurrence_time) ou nome
WITH slots AS (
  SELECT
    b.data_culto,
    b.slot,
    b.presencial_adulto, b.presencial_kids,
    b.decisoes_presenciais, b.decisoes_online,
    b.online_pico, b.online_ds, b.online_ddus,
    b.voluntarios,
    s.id   AS service_type_id,
    s.name AS service_type_name,
    s.recurrence_time
  FROM _backfill_cultos b
  LEFT JOIN public.vol_service_types s ON (
    (b.slot = 'D08'    AND s.recurrence_day = 0 AND s.recurrence_time = '08:30:00') OR
    (b.slot = 'D10'    AND s.recurrence_day = 0 AND s.recurrence_time = '10:00:00') OR
    (b.slot = 'D11'    AND s.recurrence_day = 0 AND s.recurrence_time = '11:30:00') OR
    (b.slot = 'D19'    AND s.recurrence_day = 0 AND s.recurrence_time = '19:00:00') OR
    (b.slot = 'Quarta' AND s.name ILIKE '%quarta%') OR
    (b.slot = 'AMI'    AND s.name ILIKE 'ami') OR
    (b.slot = 'BRIDGE' AND s.name ILIKE 'bridge')
  )
  WHERE s.id IS NOT NULL
)
